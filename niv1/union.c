#include <unistd.h>

void	union(char *s1, char *s2)
{
	int	i;
	int	tab[255] = {0};

	i = 0;
	while (s1[i])
	{
		if (tab[s1[i]] == 0)
		{
			tab[s1[i]] = 1;
			write(1, &s1[i], 1);
		}
		i++;
	}
	i = 0;
	while (s2[i])
	{
		if (tab[s2[i]] == 0)
		{
			write(1, &s2[i], 1);
			tab[s2[i]] = 1;
		}
		i++;
	}
}
