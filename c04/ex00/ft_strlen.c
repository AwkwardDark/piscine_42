#include <stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
		i++;
	return (i);
}

int	main(int ac, char **av)
{
	char	*str;

	str = av[1];
	if (ac == 2)
		printf("%d\n", ft_strlen(str));
	return (0);
}	
