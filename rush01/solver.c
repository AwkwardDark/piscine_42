#include <stdio.h>

int	checktab(int tab[4][4], int input[16]);

int	solver(int tab[4][4], int input[16])
{
	int	perm[24][4] = {{0, 1, 2, 3}, {0, 1, 3, 2}, {0, 2, 1, 3}, {0, 2, 3, 1}, {0, 3, 1, 2}, {0, 3, 2, 1}, {1, 0, 2, 3}, {1, 0, 3, 2}, {1, 2, 0, 3}, {1, 2, 3, 0}, {1, 3, 0, 2}, {1, 3, 2, 0}, {2, 0, 1, 3}, {2, 0, 3, 1}, {2, 1, 0, 3}, {2, 1, 3, 0}, {2, 3, 0, 1}, {2, 3, 1, 0}, {3, 0, 1, 2}, {3, 0, 2, 1}, {3, 1, 0, 2}, {3, 1, 2, 0}, {3, 2, 0, 1}, {3, 2, 1, 0}};
	int	i;
	int	j;
	int	k;
	int	m;

	i = 0;
	while (i < 24){
		j = 0;
		while (j < 24){
			k = 0;
			m = 0;
			while (k < 4){
				tab[k][m] = ((perm[i][m] + perm[j][k]) % 4) + 1;
				m++;
				if (m == 4){	
					k++;
					m = 0;
				}
			}
			if (checktab(tab, input) == 1)
				return (1);
			j++;
			}
		i++;
		}
	return (0);
}
